    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, MicroPost::class);
    }

    public function findAllWithComments()
    {
        return $this->createQueryBuilder('p') // p = post
        ->leftJoin('p.comments', 'c')     // Post'un comments tablosuyla birleş (c = comment)
        ->orderBy('p.created', 'DESC')
        ->addSelect('c')                  // Yorum verilerini de hafızaya al (Select'e ekle)
        ->getQuery()
            ->getResult();
    }

